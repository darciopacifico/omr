package com.msaf.validador.consultaonline.exceptions;

public class BaseValidadorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -116375342064602708L;

	public BaseValidadorException() {
		super();
	}

	public BaseValidadorException(String arg0) {
		super(arg0);
	}

	public BaseValidadorException(Throwable arg0) {
		super(arg0);
	}

	public BaseValidadorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}